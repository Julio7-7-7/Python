import math

print(round(1.3))  # redondea
print(abs(-1))  # ignora el signo

print(math.ceil(1.1))  # redondea al entero más cercano hacia arriba
print(math.floor(1.1))  # redondea al entero más cercano hacia abajo
print(math.isnan(1.1))  # se pregunta si no es número
print(math.pow(2, 4))  # elevar a una potencia
print(math.sqrt(9))  # saca raíz cuadrada
